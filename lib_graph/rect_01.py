import graph

graph.penColor(0, 0, 0)  # цвет обводки
graph.penSize(5)  # изменяет размер обводки ( 1 значение = 1 пиксель)
graph.brushColor("brown")  # цвет заливки
graph.rectangle(10, 20, 50, 60)  # фигура и ее параметры (x1, y1, x2, y2)

graph.run()  # вызов окна, чтобы видеть фигру (открывает окно с получившимся кодом
